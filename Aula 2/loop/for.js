// for repetition
// Resultará em Repetição de 1 a 4, pois foi pedido até menor que 5
// Repetição 1 | Repetição 2 | Repetição 3 | Repetição 4
for (let i = 1; i < 5; i++) {
  console.log("Repetição " + i);
}
// Resultará em Repetição de 1 a 5, pois foi pedido até menor e igual a 5
// Repetição 1 | Repetição 2 | Repetição 3 | Repetição 4 | Repetição 5
for (let i = 1; i <= 5; i++) {
  console.log("Repetição " + i);
}
